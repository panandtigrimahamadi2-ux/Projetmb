    private void btnajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajouterActionPerformed

        btnnouveau.setEnabled(true);
        btnajouter.setEnabled(false);
        txtmotif.setEnabled(false);
        txtsomme.setEnabled(false);
        String motif = txtmotif.getText().trim();
    String sommeStr = txtsomme.getText().trim();

    if (motif.isEmpty() || sommeStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs.");
        return;
    }

    try {
        double montant = Double.parseDouble(sommeStr);
        String date = new SimpleDateFormat("yyyy-MM-dd").format(new Date());

        // ✅ Utiliser la nouvelle méthode depense.ajouter qui gère id_caisse
        depense.ajouter(motif, montant, date);

        JOptionPane.showMessageDialog(this, "Dépense ajoutée avec succès !");

        // Mettre à jour le total des dépenses
        double total = depense.calculerTotal();
        txttotal.setText(String.valueOf(total));

        // Rafraîchir la liste des dépenses
        rafraichirListeDepenses();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Aucune caisse ouverte.");
    }

    }//GEN-LAST:event_btnajouterActionPerformed

