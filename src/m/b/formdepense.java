    private void btnArchivesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArchivesActionPerformed
        // TODO add your handling code here:
         try {
        ResultSet rs = depense.getDepensesArchive();
        DefaultTableModel model = new DefaultTableModel(new String[]{"Motif", "Montant", "Date Dépense", "Caisse", "Date Archive"}, 0);

        while (rs.next()) {
            Object[] row = {
                rs.getString("motif"),
                rs.getDouble("montant"),
                rs.getDate("date_depense"),
                rs.getString("id_caisse"),
                rs.getTimestamp("date_archive")
            };
            model.addRow(row);
        }

        tabdepense.setModel(model);
        rs.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erreur lors du chargement des archives.");
    }

    }//GEN-LAST:event_btnArchivesActionPerformed

