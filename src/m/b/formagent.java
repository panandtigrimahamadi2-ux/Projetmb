    private void chargerListeAgents() throws SQLException {
    DefaultTableModel model = (DefaultTableModel) tabagent.getModel();
    model.setRowCount(0);

    Vector<Vector<Object>> lignes = agent.getListeAgents();
    for (Vector<Object> ligne : lignes) {
        model.addRow(ligne);
    }
}
    
    private void chargerProfils() {
    cmbprofil.removeAllItems();
    Vector<String> profils = utilisateur.getListeProfils(); // ou une autre classe selon ta structure
    for (String p : profils) {
        cmbprofil.addItem(p);
    }
}
    
    private boolean verifierChampsObligatoires() {
    if (txtnom.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez saisir le nom de l'agent.");
        txtnom.requestFocus();
        return false;
    }

    if (ddate.getDate() == null) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner une date de naissance.");
        ddate.requestFocus();
        return false;
    }

    if (cmbprofil.getSelectedIndex() == -1) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un profil.");
        cmbprofil.requestFocus();
        return false;
    }

    if (cmbtypesalaire.getSelectedIndex() == -1) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un type de salaire.");
        cmbtypesalaire.requestFocus();
        return false;
    }

    String typeSalaire = cmbtypesalaire.getSelectedItem().toString();
    if ("par mois".equalsIgnoreCase(typeSalaire)) {
        if (txtsalaire.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez saisir le salaire mensuel.");
            txtsalaire.requestFocus();
            return false;
        }
    } else {
        if (txtprixbouteille.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez saisir le prix par bouteille.");
            txtprixbouteille.requestFocus();
            return false;
        }
    }

    return true;
}

