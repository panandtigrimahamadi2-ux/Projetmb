    private void btnvaliderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvaliderActionPerformed
        // TODO add your handling code here:
        
    btnsupprimer.setEnabled(false);
    btnmodifier.setEnabled(false);
    btnvalider.setEnabled(false);
    btnimprimer.setEnabled(true);
    tabagent.setEnabled(true);
    txtnom.setEnabled(false);
    ddate.setEnabled(false);
    cmbprofil.setEnabled(false);
    cmbtypesalaire.setEnabled(false);
    btnnouveau.setEnabled(true);
        String nom = txtnom.getText().trim();
        
if (ddate.getDate() == null) {
    JOptionPane.showMessageDialog(this, "Veuillez sélectionner une date de naissance.");
    return;
}

java.sql.Date dateNaissance = new java.sql.Date(ddate.getDate().getTime());
    String profil = cmbprofil.getSelectedItem() != null ? cmbprofil.getSelectedItem().toString() : "";
    String typeSalaire = cmbtypesalaire.getSelectedItem() != null ? cmbtypesalaire.getSelectedItem().toString() : "";
if (!verifierChampsObligatoires()) {
    return;
}
    if (nom.isEmpty() || profil.isEmpty() || typeSalaire.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs obligatoires.");
        return;
    }

    double salaire = 0;
    double prixParBouteille = 0;

    try {
        if ("par mois".equalsIgnoreCase(typeSalaire)) {
            salaire = Double.parseDouble(txtsalaire.getText().trim());
        } else if ("par bouteille".equalsIgnoreCase(typeSalaire)) {
            prixParBouteille = Double.parseDouble(txtprixbouteille.getText().trim());
        }

        agent.enregistrer(nom, (java.sql.Date) dateNaissance, profil, typeSalaire, salaire, prixParBouteille);
        JOptionPane.showMessageDialog(this, "Agent enregistré avec succès !");
        btnnouveau.doClick();
        chargerListeAgents();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Veuillez saisir des montants valides.");
    }   catch (SQLException ex) {
            Logger.getLogger(formagent.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnvaliderActionPerformed

