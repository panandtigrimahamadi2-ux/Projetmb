    private void cmbboissonFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cmbboissonFocusGained
        // TODO add your handling code here:
                                              
    try {
        Connection c = (Connection) connexionbd.seconnecter();
        Statement st = (Statement) c.createStatement();

        // Vérifie bien que ta colonne dans la table s'appelle "nom"
        ResultSet rs = (ResultSet) st.executeQuery("SELECT nom FROM boisson ORDER BY nom");

        // Vider la liste avant de recharger
        cmbboisson.removeAllItems();

        // Ajouter chaque boisson dans la liste déroulante
        while (rs.next()) {
            cmbboisson.addItem(rs.getString("nom"));
        }

        rs.close();
        st.close();
        c.close();

    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(formstock.class.getName()).log(Level.SEVERE, null, ex);
    }

    }//GEN-LAST:event_cmbboissonFocusGained

