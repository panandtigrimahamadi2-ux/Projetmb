    private void btnvaliderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvaliderActionPerformed
        // TODO add your handling code here:  
        txtnombre.setEnabled(false);
        cmbboisson.setEnabled(false);
        btnvalider.setEnabled(false);
        btnnouveau.setEnabled(true);
        ch1.setEnabled(false);
        ch6.setEnabled(false);
        ch12.setEnabled(false);
        ch24.setEnabled(false);
        
    try {
        String boisson = cmbboisson.getSelectedItem().toString();
        int nombres = Integer.parseInt(txtnombre.getText().trim());

        // Vérifier le type de caisse
        int multiplicateur = 1;
        if (ch24.isSelected()) multiplicateur = 24;
        else if (ch12.isSelected()) multiplicateur = 12;
        else if (ch6.isSelected()) multiplicateur = 6;
        else if (ch1.isSelected()) multiplicateur = 1;

        int nombre = nombres * multiplicateur;

        // 👉 Enregistrer dans la base
        stock.validerApprovisionnement(boisson, nombre);

        // 👉 Actualiser le tableau
        DefaultTableModel tm = (DefaultTableModel) tablistboisson.getModel();
        tm.setRowCount(0);
        stock.actualiserStock(tm);

        JOptionPane.showMessageDialog(this, "Approvisionnement validé avec succès !");

        // Réinitialiser les champs
        txtnombre.setText("");
        ch1.setSelected(false);
        ch6.setSelected(false);
        ch12.setSelected(false);
        ch24.setSelected(false);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Veuillez entrer un nombre valide.");
    }   catch (SQLException ex) {
            Logger.getLogger(formstock.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnvaliderActionPerformed

