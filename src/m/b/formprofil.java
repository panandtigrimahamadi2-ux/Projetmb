private void chargerComboProfil() {
    cmbprofil.removeAllItems(); // Vide le ComboBox

    Vector<String> profils = profil.getListeProfils(); // Appel à la méthode métier
    if (profils != null && !profils.isEmpty()) {
        for (String p : profils) {
            cmbprofil.addItem(p); // Ajoute chaque profil au ComboBox
        }
    } else {
        JOptionPane.showMessageDialog(this, "Aucun profil trouvé dans la base de données.");
    }
}

