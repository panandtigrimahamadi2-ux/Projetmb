    private void cmbprofilFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cmbprofilFocusGained
        // TODO add your handling code here:
   try {
        Connection c = (Connection) connexionbd.seconnecter();
        Statement st = c.createStatement();
        ResultSet rs = st.executeQuery("SELECT droit FROM profil ORDER BY droit");

        cmbprofil.removeAllItems(); // Vider la liste déroulante

        while (rs.next()) {
            cmbprofil.addItem(rs.getString("droit")); // Ajouter chaque profil
        }

        rs.close();
        st.close();
        c.close();
    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(formprofil.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Erreur lors du chargement des profils.");
    }

    }//GEN-LAST:event_cmbprofilFocusGained

