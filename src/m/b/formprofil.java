    private void btnsupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsupprimerActionPerformed
        // TODO add your handling code here:
        btnnouveau.setEnabled(true);
       String droit = cmbprofil.getSelectedItem() != null ? cmbprofil.getSelectedItem().toString() : null;
    if (droit == null || droit.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un profil à supprimer.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Supprimer le profil \"" + droit + "\" ?", "Confirmation", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        profil.supprimer(droit);
        cmbprofil.removeItem(droit); // Retirer du ComboBox
        chargerComboProfil(); // Mettre à jour le tableau
        reinitialiserCasesListeProfil();
        JOptionPane.showMessageDialog(this, "Profil supprimé.");
    }

    }//GEN-LAST:event_btnsupprimerActionPerformed

